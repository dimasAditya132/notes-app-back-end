const { nanoid } = require('nanoid');
const notes = require('./notes');

const addNoteHandler = (request, h) => {
    const { title, tags, body } = request.payload;

    const id = nanoid(16);
    const createdAt = new Date().toISOString();
    const updateAt = createdAt;

    const newNote = {
        title,
        tags,
        body,
        id,
        createdAt,
        updateAt,
    };
    // TypeError: notes.push is not a function
    notes.push(newNote);

    const isSuccess = notes.filter((note) => note.id === id).length > 0;

    if (isSuccess) {
        const response = h.response({
            status: 'success',
            message: 'Catatan berhasil ditambahkan',
            data: {
                noteId: id,
            },
        });
        // CORS Manual (CommonJS/Javascript)
        // response.header('Access-Control-Allow-Origin', 'http://notesapp-v1.dicodingacademy.com%27/);
        response.code(201);
        return response;
    }

    const response = h.response({
        status: 'fail',
        message: 'Catatan gagal ditambahkan',
    });
    response.code(500);
    return response;
};

const getAllNotesHandler = () => ({
    status: 'success',
    data: {
        notes,
    },
});

const getNoteByIdHandler = (request, h) => {
    const { id } = request.params;

    const note = notes.filter((n) => n.id === id)[0];

    //Kita pastikan dulu objek note tidak bernilai undefined, bila defined kembalikan respon gagal
    if (note !== undefined) {
        return {
            status: 'success',
            data: {
                note,
            },
        };
    }

    const response = h.response({
        status: 'fail',
        message: 'Catatan tidak ditemukan',
    });
    response.code(404);
    return response;
};

const editNoteByIdHandler = (request, h) => {
    const { id } = request.params;

    const { title, tags, body } = request.payload;

    //Kita perlu memperbarui juga nilai dari properti updatedAt.
    //Jadi, dapatkan nilai terbaru dengan menggunakan new Date().toISOString().
    const updateAt = new Date().toISOString();

    //Pertama, dapatkan dulu index array pada objek catatan sesuai id
    //yang ditentukan. Untuk melakukannya, gunakanlah method array findIndex().
    const index = notes.findIndex((note) => note.id === id);

    //Bila id ditemukan index akan bernilai array
    //Bila tidak ditemukan index akan bernilai -1

    if (index !== -1) {
        notes[index] = {
            ...notes[index],
            title,
            tags,
            body,
            updateAt,
        };

        const response = h.response({
            status: 'success',
            message: 'Catatan berhasil diperbarui',
        });
        response.code(200);
        return response;
    }

    const response = h.response({
        status: 'fail',
        message: 'Gagal memperbarui catatan. Id tidak ditemukan',
    });
    response.code(404);
    return response;
};

const deleteNoteByIdHandler = (request, h) => {
    //apatkan dulu nilai id yang dikirim
    //melalui path parameter.
    const { id } = request.params;

    //dapatkan index dari objek catatan sesuai
    //dengan id yang didapat.
    const index = notes.findIndex((note) => note.id === id);

    if (index !== -1) {
        notes.splice(index, 1);
        const response = h.response({
            status: 'success',
            message: 'Catatan berhasil dihapus',
        });
        response.code(200);
        return response;
    }

    const response = h.response({
        status: 'fail',
        message: 'Catatan gagal dihapus. Id tidak ditemukan',
    });
    response.code(404);
    return response;
};

module.exports = {
    addNoteHandler,
    getAllNotesHandler,
    getNoteByIdHandler,
    editNoteByIdHandler,
    deleteNoteByIdHandler,
};
